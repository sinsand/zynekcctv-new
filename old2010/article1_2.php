        <strong> <span class="style33">&nbsp;<img src="images/icon.gif" width="7" height="7" /> </span></strong><span class="style33">งบประมาณ</span> การตั้งราคาเป็นส่วนสำคัญในการตัดสินใจเลือกซื้อคอมพิวเตอร์       แต่ถ้าหากเรารู้จักอุปกรณ์เลือกดูคุณสมบัติดีแล้วจะเห็นว่าคุณสมบัติอาจเหมือนกันแต่ราคาต่างกันมาก       นั่นเพราะส่วนหนึ่งมาจากวัสดุที่ใช้งานอาจคงทนดีกว่า และการออกแบบดีไซต์มีฟังก์ชันมารองรับได้มากกว่า       ดังนั้นการตั้งราคาจึงต้องดูความเหมาะสมทั้งด้านคุณสมบัติและความทนทานต่อการใช้งาน<br />
        <br>
        <strong><span class="style33">&nbsp;<img src="images/icon.gif" width="7" height="7" /> </span></strong><span class="style33">ลักษณะงานที่นำไปใช้งาน</span> การนำไปใช้งานให้ได้ประโยชน์ต่อการใช้งานได้มากก็ต้องเลือกอุปกรณ์ให้ตรงกับคุณสมบัติที่จะนำไปใช้       เช่นงานด้านกราฟฟิก ก็ต้องเลือกการ์ดจอที่มี GPU แยกและมี RAM ในการช่วยหน่วยความจำให้อ่านและเขียนคำสั่งได้ไวเพราะกราฟฟิกเป็นการ       Encoder และ Decoder ในเวลาเดียวกัน และถ้าต้องการนำไปใช้งานด้านการทำงานด้วยโปรแกรมพร้อมๆกันหลายโปรแกรมก็ต้องเลือก CPU       ที่มีระบบทำงานได้แบบ Multi-threads       หรือ Hyper-Treading และมีหน่วยกักเก็บข้อมูล(L Caches) สูงเช่น L3=12Mb<br />
        <br>
        <strong><span class="style33">&nbsp;<img src="images/icon.gif" width="7" height="7" /> </span></strong><span class="style33">ยี่ห้อและการรับประกัน</span> การหาคุณสมบัติที่ดีก็ต้องเป็นที่ยอมรับของสากล       จึงต้องเปรียบเทียบความแตกต่างของแต่ละแบรนด์อุปกรณ์คอมพิวเตอร์ส่วนใหญ่นำเข้ามาผลิตชิ้นส่วนในเมืองไทย       ซึ่งการที่บริษัทจะรับประกันนานไม่ใช่จะเป็นการดีเสมอเพราะผู้ผลิตจะทราบถึงอุปกรณ์ที่นำเข้ามาผลิตกับระยะเวลาในการรับประกัน       บางครั้งพอหมดประกันอุปกรณ์อาจเกิดปัญหาช่วงนั้นก็ได้       ดังนั้นถ้าต้องเลือกอุปกรณ์ก็ต้องเลือกศูนย์รับประกันและเคลมสินค้าด้วย เพราะถ้าอุปกรณ์ที่เกิดปัญหาเวลาส่งให้ศูนย์เคลมอาจใช้เวลานานหรือซ่อมแซมนำของเก่าที่ใช้ได้มาเปลี่ยนให้แทน       ถ้าบริษัทที่มั่นใจว่าสินค้าดีจริงบริษัทก็จะทำการเปลี่ยนใหม่ให้ถ้าหากตรวจพบอาการเสียจริง<br />
        <br>
        <strong><span class="style33">&nbsp;<img src="images/icon.gif" width="7" height="7" /> </span></strong><span class="style33">บริการของผู้ขาย</span> เป็นจุดที่ผู้เลือกซื้อจะต้องสังเกต       อุปกรณ์คอมพิวเตอร์ต้องอาศัยความเข้าใจและความรู้ด้านคอมพิวเตอร์พอสมควรจึงต้องสามารถให้ข้อมูลได้ถูกต้องและนำไปใช้ได้จริง       ดังนั้นผู้ซื้อต้องสังเกตผู้ขายอุปกรณ์คอมพิวเตอร์ว่าขณะให้บริการมีการแนะนำรุ่นที่ดีกว่าหรือปฏิเสธการเลือกอุปกรณ์ที่เหมาะสมกว่าหรือไม่       เพราะอุปกรณ์ที่ใช้งานอาจมีรุ่นเก่าใหม่ในแต่ละวันไม่เท่ากัน       และมีการทดสอบการใช้งานพร้อมการแนะนำโปรแกรมที่ใช้งานได้อย่างเข้าใจรวมถึงการบริการหลังการขายกรณีเมื่อเกิดปัญหา ซึ่งอาจเกิดอุปกรณ์บกพร่องไม่สามารถแก้ได้<br />
        <br>
        <strong><span class="style33">&nbsp;<img src="images/icon.gif" width="7" height="7" /> </span></strong><span class="style33">ความรู้ในการใช้งานคอมพิวเตอร์</span> การเรียนรู้ด้วยตัวเองสำหรับคอมพิวเตอร์จะทำให้เข้าใจมากกว่าการถามจากคนอื่นหรือเรียนในห้องเรียน       เพราะโปรแกรมคอมพิวเตอร์มาจากคนที่คิดเงื่อนไขในการจะให้เครื่องคอมพิวเตอร์สามารถคำนวนคำสั่งพร้อมกันหลายล้านคำสั่งในเวลาเดียว จึงเกิดการใช้งานที่เคยชินและสร้างเงื่อนไขในการใช้งานให้กับตนเอง       ดังนั้นการรู้จักหน้าที่การทำงานของอุปกรณ์แต่ละตัวจะทำให้เรารู้จักที่จะใช้งานของหน้าที่นั้นๆ เมื่อเกิดปัญหาก็จะตั้งสมมติฐานได้ว่าลักษณะที่เกิดปัญหามาจากการทำงานของหน้าที่ใด       จึงแก้ปัญหานั้นได้<br>
